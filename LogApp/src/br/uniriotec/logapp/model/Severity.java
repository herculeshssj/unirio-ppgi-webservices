package br.uniriotec.logapp.model;

public class Severity {

	private String name;
	private int level;
	
	public Severity() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}
}
